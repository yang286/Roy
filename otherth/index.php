						<p>今天2:45看欧洲杯意大利vs法国的比赛，看完大概在5点钟不到，然后上个WC准备睡觉，在WC中突然听到宿舍墙外面有人在跳的声音(最后才明白跳的声音是因为此贼搭了个梯子正往我们窗户上爬，听到我们去睡觉的声音可能以为我们发现他了，所以从梯子上跳下去准备逃命)，当初以为又是猫子，不过想想不对，猫子没这么大的动静，这些反应都是在一两秒钟内完成的。于是从WC的窗户向外看一眼，哇，果然有发现。</p>
						<p>一个小平头，穿着一双破拖鞋，身高一米六左右的二十八九岁的小青年，正弓着腰一小步一小步的移动。Who is he? 这个问题想了一秒钟左右，反应过来，贼。</p>
						<p>当时我也慌了，不知道如何是好，此时的笨贼完全没有发现我。我可以做的全部选择如下：<br />
						<ol class="listA"><li> 最聪明的做法，不惊到他，悄悄把宿舍通往楼下的门打开，因为我们宿舍的门晚上是锁上的，所以小偷只能在宿舍外面搭梯子往上爬。打开门之后再劲喊同事抓贼。这样此贼就定被我们抓住。</li>
						<li>最笨的方法，也是我所用的方法。就是在我看到贼的几秒钟后，大喊抓小偷，小偷发现之后狂跑，我跟同事要去抓贼先要打开宿舍通往楼下大门，加上同事起床时间，总共要花30秒时间，这样贼肯定会跑得无影无踪。</li></ol></p>
						<p>当时在那种情况下，没来得及像现在这样想的这么清楚，全是乱的，所以就本能的用了最笨的方法，喊人来抓往它。因为自己也有点怕。在我喊的那一瞬间把小偷吓的差点摔倒，这点倒让我有点高兴，哈哈。喊了之后此贼才发现我，他胆倒是非常大，往我所在窗户上面扔东西想要我老命，扔的什么鸟东西当时紧张所以没看清楚。扔完东西之后肯定是使劲逃命去了，同事个个穿着短裤起来了，不过全被堵在门口出不去，门没开，我当时没敢开，怕小贼又搞块大砖头上来取我性命。然后让同事快开门，开完门跑到工厂大铁门外一看，早就没影了。</p>
						<p>
						写下此记意在提醒自己或是大家，下次遇到类似情况一定不要慌张紧张，<span class="red">如果是在慌张或是紧张情况下，我们做出的选择或是判断往往都是非常笨或是非常错的选择。</span>
